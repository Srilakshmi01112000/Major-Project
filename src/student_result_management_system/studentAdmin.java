    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        // TODO add your handling code here:
        try{
            String usn=jTextField1.getText();
          Class.forName("com.mysql.cj.jdbc.Driver");  
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/srm","root","01112000");
            Statement st=con.createStatement();
            if(jComboBox1.getSelectedItem()=="CSE"){
            ResultSet rs=st.executeQuery("select * from student inner join CSEresults where student.USN='"+usn+"'and CSEresults.USN='"+usn+"'");
            if(rs.next()){
                setVisible(false);
                new studenthomecse(usn).setVisible(true);
            }
            else{
                JOptionPane.showMessageDialog(null,"Incorrect USN");
            }
            }
            if(jComboBox1.getSelectedItem()=="Mechanical"){
            ResultSet rs=st.executeQuery("select * from student inner join Mechresults where student.USN='"+usn+"'and Mechresults.USN='"+usn+"'");
            if(rs.next()){
                setVisible(false);
                new studenthomemech(usn).setVisible(true);
            }
            else{
                JOptionPane.showMessageDialog(null,"Incorrect USN");
            }
            }
            if(jComboBox1.getSelectedItem()=="Civil"){
            ResultSet rs=st.executeQuery("select * from student inner join civilresults where student.USN='"+usn+"'and civilresults.USN='"+usn+"'");
            if(rs.next()){
                setVisible(false);
                new studenthomecivil(usn).setVisible(true);
            }
            else{
                JOptionPane.showMessageDialog(null,"Incorrect USN");
            }
            }
            } 
        catch(Exception e){
         JOptionPane.showMessageDialog(null,e.toString());
        }
        
        
    }//GEN-LAST:event_jButton1ActionPerformed

