    private void formComponentShown(java.awt.event.ComponentEvent evt) {//GEN-FIRST:event_formComponentShown
        // TODO add your handling code here:
        try{
       String usn=jTextField3.getText();
          Class.forName("com.mysql.cj.jdbc.Driver");  
            Connection con=DriverManager.getConnection("jdbc:mysql://localhost:3306/srm","root","01112000");
            Statement st=con.createStatement(); 
            ResultSet rs=st.executeQuery("select * from student inner join civilresults where student.USN='"+usn+"'and civilresults.USN='"+usn+"'");
            if(rs.next()){
                jTextField1.setText(rs.getString(3));
                 jTextField2.setText(rs.getString(1));
                 jTextField4.setText(rs.getString(5));
                 jTextField5.setText(rs.getString(4));
                 jTextField6.setText(rs.getString(6));
                 
                 jTextField7.setText(rs.getString(8));
                jTextField8.setText(rs.getString(9));
                jTextField9.setText(rs.getString(10));
                jTextField10.setText(rs.getString(11));
                
                
                int s1=Integer.parseInt( jTextField7.getText());
                 int s2=Integer.parseInt( jTextField8.getText());
                 int s3=Integer.parseInt( jTextField9.getText());
                 int s4=Integer.parseInt( jTextField10.getText());
                 
                 int result=s1+s2+s3+s4;
                 int avg=result/4;
                 jTextField11.setText(String.valueOf(result));
                 if(avg>35){
                     jTextField12.setText("PASS");
                 }
                 else{
                     jTextField12.setText("FAIL");
                 }
            }
        
        }
         catch(Exception e){
         JOptionPane.showMessageDialog(null,e.toString());
        }
        
    }//GEN-LAST:event_formComponentShown

